#include<iostream>
using namespace std;
int main()
{
    int a;
    a = 12;

    cout << "Size of int is : " << sizeof(a)<<endl;

    float f;
    cout << "Size of float is : " << sizeof(f) << endl;

    double d;
    cout << "Size of double is : " << sizeof(d) << endl;

    char c;
    cout << "Size of char is : " << sizeof(c) << endl;

    short int si;
    cout << "Size of short is : " << sizeof(si) << endl;

    long int li;
    cout << "Size of long is : " << sizeof(li) << endl;

}
